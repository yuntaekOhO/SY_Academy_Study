public class OperatorMain13 {

	public static void main(String[] args) {
		
		System.out.println("====대입연산자====");
		
		//대입연산자 '=' 
		int a = 5;
		int b = 7;
		
		//대입연산자(대입연산자 + 산술연산자)
		a += b; // a = a + b
		System.out.println("a = " + a);
		
		a -= b; // a = a - b
		System.out.println("a = " + a);
		
		a *= b; // a = a * b
		System.out.println("a = " + a);
		
		a /= b; // a = a / b
		System.out.println("a = " + a);
		
		a %= b; // a = a % b
		System.out.println("a = " + a);
		
	}

}
